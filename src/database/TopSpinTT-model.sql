                            <p>O backhand é executado do lado oposto ao dominante (esquerda para destros, direita para canhotos). Ele pode ser feito com o braço à frente do corpo, utilizando principalmente o antebraço e o punho para gerar velocidade e efeito. No grip clássico, o backhand é bastante natural, enquanto no caneta tradicional pode ser mais limitado, sendo compensado pelo uso do classineta ou por movimentação rápida para usar o forehand.</p>
